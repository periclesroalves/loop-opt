#!/bin/sh -e

for i in anagram backprop bc ft ks yacr2
do
  cd $i
  echo "####################################################################"
  echo "#### Compiling $i..."
  echo "####################################################################"
  make clean
  make
  echo "####################################################################"
  echo "#### Testing $i..."
  echo "####################################################################"
  make test
  make clean
  cd ..
done

